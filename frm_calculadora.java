    private void txt_pantallaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_pantallaKeyPressed
        if(evt.getKeyCode()==KeyEvent.VK_NUMPAD1 || evt.getKeyCode()==KeyEvent.VK_1){
            this.txt_pantalla.setText(this.txt_pantalla.getText()+"1");    
        }
        if(evt.getKeyCode()==KeyEvent.VK_NUMPAD2 || evt.getKeyCode()==KeyEvent.VK_2){
            this.txt_pantalla.setText(this.txt_pantalla.getText()+"2");
            
        }
        if(evt.getKeyCode()==KeyEvent.VK_NUMPAD3 || evt.getKeyCode()==KeyEvent.VK_3){
            this.txt_pantalla.setText(this.txt_pantalla.getText()+"3");
            
        }
        if(evt.getKeyCode()==KeyEvent.VK_NUMPAD4 || evt.getKeyCode()==KeyEvent.VK_4){
            this.txt_pantalla.setText(this.txt_pantalla.getText()+"4");
            
        }
        if(evt.getKeyCode()==KeyEvent.VK_NUMPAD5 || evt.getKeyCode()==KeyEvent.VK_5){
            this.txt_pantalla.setText(this.txt_pantalla.getText()+"5");
            
        }
        if(evt.getKeyCode()==KeyEvent.VK_NUMPAD6 || evt.getKeyCode()==KeyEvent.VK_6){
            this.txt_pantalla.setText(this.txt_pantalla.getText()+"6");
            
        }
        if(evt.getKeyCode()==KeyEvent.VK_NUMPAD7 || evt.getKeyCode()==KeyEvent.VK_7){
            this.txt_pantalla.setText(this.txt_pantalla.getText()+"7");
            
        }
        if(evt.getKeyCode()==KeyEvent.VK_NUMPAD8 || evt.getKeyCode()==KeyEvent.VK_8){
            this.txt_pantalla.setText(this.txt_pantalla.getText()+"8");
            
        }
        if(evt.getKeyCode()==KeyEvent.VK_NUMPAD9 || evt.getKeyCode()==KeyEvent.VK_9){
            this.txt_pantalla.setText(this.txt_pantalla.getText()+"9");
            
        }
        if(evt.getKeyCode()==KeyEvent.VK_NUMPAD0 || evt.getKeyCode()==KeyEvent.VK_0){
            this.txt_pantalla.setText(this.txt_pantalla.getText()+"0");
            
        }
        if(evt.getKeyCode()==KeyEvent.VK_PERIOD){
            if(!(this.txt_pantalla.getText().contains("."))){
                this.txt_pantalla.setText(this.txt_pantalla.getText()+".");
            }
        }
        if(evt.getKeyCode()==KeyEvent.VK_ADD ||evt.getKeyCode()==KeyEvent.VK_PLUS){
            this.x1=Double.parseDouble(this.txt_pantalla.getText());
            this.lbl_op.setText(Double.toString(this.x1)+"  +");
            this.operacion="+";
            this.txt_pantalla.setText("");
        }
        if(evt.getKeyCode()==KeyEvent.VK_SUBTRACT ||evt.getKeyCode()==KeyEvent.VK_MINUS){
            this.x1=Double.parseDouble(this.txt_pantalla.getText());
            this.lbl_op.setText(Double.toString(this.x1)+"  -");
            this.operacion="-";
            this.txt_pantalla.setText("");
        }
        if(evt.getKeyCode()==KeyEvent.VK_MULTIPLY){
            this.x1=Double.parseDouble(this.txt_pantalla.getText());
            this.lbl_op.setText(Double.toString(this.x1)+"  *");
            this.operacion="*";
            this.txt_pantalla.setText("");
        }
        if(evt.getKeyCode()==KeyEvent.VK_DIVIDE||evt.getKeyCode()==KeyEvent.VK_SLASH){
            this.x1=Double.parseDouble(this.txt_pantalla.getText());
            this.lbl_op.setText(Double.toString(this.x1)+"  /");
            this.operacion="/";
            this.txt_pantalla.setText("");
        }
        if(evt.getKeyCode()==KeyEvent.VK_EQUALS||evt.getKeyCode()==KeyEvent.VK_ENTER){
             this.x2=Double.parseDouble(this.txt_pantalla.getText());
        
            switch(this.operacion){
                case "+":{
                    this.x3=this.x1+this.x2;
                    this.txt_pantalla.setText(Double.toString(this.x3));
                    this.lbl_op.setText("");
                }break;
                case "-":{
                    this.x3=this.x1-this.x2;
                    this.txt_pantalla.setText(Double.toString(this.x3));
                    this.lbl_op.setText("");
                }break;
                case "*":{
                    this.x3=this.x1*this.x2;
                    this.txt_pantalla.setText(Double.toString(this.x3));
                    this.lbl_op.setText("");
                }break;
                case "/":{
                    if(this.x2==0){
                        this.lbl_adv.setText("Ningun numero puede ser dividido en 0, intentelo de nuevo");
                        this.txt_pantalla.setText("");
                    }else{
                        this.x3=this.x1/this.x2;
                        this.txt_pantalla.setText(Double.toString(this.x3));
                        this.lbl_op.setText("");
                    }

                }break;
            }
        }
        if(evt.getKeyCode()==KeyEvent.VK_BACK_SPACE){
            this.txt_pantalla.setText("");
        }
        
    }//GEN-LAST:event_txt_pantallaKeyPressed

