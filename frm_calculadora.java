    private void btn_igualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_igualActionPerformed
        // TODO add your handling code here:
        this.x2=Double.parseDouble(this.txt_pantalla.getText());
        
        switch(this.operacion){
            case "+":{
                this.x3=this.x1+this.x2;
                this.txt_pantalla.setText(Double.toString(this.x3));
                this.lbl_op.setText("");
            }break;
            case "-":{
                this.x3=this.x1-this.x2;
                this.txt_pantalla.setText(Double.toString(this.x3));
                this.lbl_op.setText("");
            }break;
            case "*":{
                this.x3=this.x1*this.x2;
                this.txt_pantalla.setText(Double.toString(this.x3));
                this.lbl_op.setText("");
            }break;
            case "/":{
                if(this.x2==0){
                    this.lbl_adv.setText("Ningun numero puede ser dividido en 0, intentelo de nuevo");
                    this.txt_pantalla.setText("");
                }else{
                    this.x3=this.x1/this.x2;
                    this.txt_pantalla.setText(Double.toString(this.x3));
                    this.lbl_op.setText("");
                }
                
            }break;
        }
        
    }//GEN-LAST:event_btn_igualActionPerformed

